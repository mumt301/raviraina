// function to create and display media table
function create_media_table(json_vals) {
    let filename = json_vals[0]['Filename']

    // only create elements once
    if (document.getElementById(filename)) {
        console.log("Already Created")
    } else {

        let table = document.createElement('table');
        table.id = filename

        // create header
        let header = Object.keys(json_vals[0]);
        let tr = table.insertRow(-1);
        for (let i = 0; i < header.length; i++) {
            let th = document.createElement("th");
            th.innerHTML = header[i];
            tr.appendChild(th);
        }

        // create table
        for (row = 0; row < json_vals.length; row++) {
            tr = document.createElement('tr');
            let columns = Object.keys(json_vals[row]);

            for (col = 0; col < columns.length; col++) {

                td = document.createElement('td');
                tr.appendChild(td);
                if (col < 6) {
                    td.innerHTML = json_vals[row][columns[col]];
                } else {
                    a = document.createElement('a');
                    a.href = json_vals[row][columns[col]];
                    a.innerHTML = "Click Here";
                    td.appendChild(a)
                }

            }
            table.appendChild(tr);
        }
        document.getElementById("compression_table").appendChild(table);
    }

}